/**
 * @param {string[]} words
 * @param {number} k
 * @return {string[]}
 */
var topKFrequent = function(words, k) {
  const hash = new Map();
  words.forEach(d => {
    if (hash.has(d)) hash.set(d, hash.get(d) + 1);
    else hash.set(d, 1);
  });
  const arr = Array.from(hash.keys()).sort((a, b) => {
    let diff = hash.get(b) - hash.get(a);
    if (diff === 0) {
      if (a < b) return -1;
      if (a > b) return 1;
      return 0;
    } else {
      return diff;
    }
  });
  return arr.slice(0, k);
};

console.log(topKFrequent(['i', 'love', 'leetcode', 'i', 'love', 'coding'], 2));
// ['i', 'love']

